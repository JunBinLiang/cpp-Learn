int to_int(string &s)
{
  int res = 0;
  for (char c : s)
  {
    res = res * 10 + (c - '0');
  }
  return res;
}

bool COMP(ve<int>& a, ve<int>& b) {
  if(a[0] == b[0]) {
      return a[1] < b[1];
  }
  return a[0] < b[0];
}


